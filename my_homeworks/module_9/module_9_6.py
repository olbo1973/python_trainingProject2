### Домашнее задание по теме "Генераторы"

def all_variants(text):
    # Получаем длину строки
    x = len(text)

    # Сначала генерируем все одиночные символы (длина 1)
    for k in range(x):
        yield text[k]  # Возвращаем по одному символу за раз

    # Генерируем подпоследовательности длиной от 2 до x
    for j in range(2, x + 1):  # j - длина подпоследовательности
        for y in range(x - j + 1):  # y - начальный индекс
            z = y + j  # Конечный индекс
            yield text[y:z]  # Возвращаем подпоследовательность


# Пример результата выполнения программы:
a = all_variants("abc")
for i in a:
    print(i)  # Печатаем все найденные подпоследовательности