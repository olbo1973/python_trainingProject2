# Задача "Вызов разом"

def apply_all_func(int_list, *functions):
    results = {}  # Создаем пустой словарь для хранения результатов
    for func in functions:  # Перебираем все функции из *functions
        results[func.__name__] = func(int_list)  # Вызываем функцию и записываем результат
    return results  # Возвращаем словарь с результатами

# Примеры использования функции
print(apply_all_func([6, 20, 15, 9], max, min))  # Вывод: {'max': 20, 'min': 6}
print(apply_all_func([6, 20, 15, 9], len, sum, sorted))  # Вывод: {'len': 4, 'sum': 50, 'sorted': [6, 9, 15, 20]}

# Этот код создает функцию `apply_all_func`, которая принимает список чисел и любое количество функций. Затем она
# вызывает каждую из функций с переданным списком и сохраняет результаты в словаре, который возвращается по завершении.